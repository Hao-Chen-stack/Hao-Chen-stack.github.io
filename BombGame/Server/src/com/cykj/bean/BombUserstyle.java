package com.cykj.bean;


public class BombUserstyle {

  private String userid;
  private int num;
  private int hat;
  private int dress;


  public String getUserid() {
    return userid;
  }

  public void setUserid(String userid) {
    this.userid = userid;
  }


  public int getNum() {
    return num;
  }

  public void setNum(int num) {
    this.num = num;
  }


  public int getHat() {
    return hat;
  }

  public void setHat(int hat) {
    this.hat = hat;
  }


  public int getDress() {
    return dress;
  }

  public void setDress(int dress) {
    this.dress = dress;
  }

}
